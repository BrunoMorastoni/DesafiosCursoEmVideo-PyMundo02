for c in range (0, 10):
    print (c)
print ('Fim')

# Último número sempre ignora, se quiser que conte até "10" é necessário que seja assim por exemplo "(1, 11)" #
for c in range (1, 11):
    print (c)
print ('Fim')

# Se quiser que pule casas apenas adicionar um número assim: #
for c in range (0, 11, 2):
    print (c)
print ('Fim')